function add(a,b){
  return(a+b)
}
function subtract(a,b){
  return((b)-(a));
}
function multiply(a,b){
  return(b*a)
}
function divide(a,b){
  return(a/b)
}
function dec(n){
  n -= 1
  return(n)
}
function inc(n){
  n += 1
  return(n)
}
function makeInt(n){
  var something = parseInt(n, 10)
  return something
}
function preserveDecimal(n){
  var something = parseFloat(n)
  return something
}
